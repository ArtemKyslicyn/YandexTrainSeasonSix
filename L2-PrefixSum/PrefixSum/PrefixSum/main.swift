import Foundation

print("Hello, World!")

/*
 Эта программа решает задачу вычисления последовательности префиксных сумм.
 По заданной последовательности чисел `a1, a2, ..., an`, она вычисляет последовательность
 префиксных сумм `b1, b2, ..., bn`, где:
 
 bj = сумма всех элементов ai от i = 1 до j (включительно).

 Ввод:
 1. В первой строке задано число `n` (1 ≤ n ≤ 10^3) — количество элементов в последовательности a.
 2. Во второй строке задано `n` целых чисел `a1, a2, ..., an` (|ai| ≤ 10^6) — элементы последовательности.

 Вывод:
 Выводится `n` целых чисел `b1, b2, ..., bn` — последовательность префиксных сумм.
 5
 10 -4 5 0 2

*/
import Foundation

// Чтение данных
let n = Int(readLine()!)! // Количество элементов в последовательности
let a = readLine()!.split(separator: " ").map { Int($0)! } // Последовательность a

// Инициализация массива для префиксных сумм
var b = [Int](repeating: 0, count: n) // Префиксные суммы

// Вычисление префиксных сумм
for i in 0..<n {
    if i == 0 {
        b[i] = a[i] // Первый элемент префиксной суммы равен первому элементу последовательности
    } else {
        b[i] = b[i - 1] + a[i] // Текущая сумма = предыдущая сумма + текущий элемент
    }
}

// Вывод результата
print(b.map { String($0) }.joined(separator: " "))
